public class CarModel {
	private int carModelId;
	private String name;
	
	public int getCarModelId() {
		
		return carModelId;
	}
	public void setCarModelId(int carModelId) {
		this.carModelId = carModelId;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
}
